
#include "stdafx.h"

#include "backendInterfaceThread.h"

#include "ProcInit.h"

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Create and launch threads. This is called at dll initialization.

void proc_initialize()
{
   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Launch program threads for simulation mode and exit.
  
   // Create and launch thread.
   BackEnd::gInterfaceThread = new BackEnd::InterfaceThread;
   BackEnd::gInterfaceThread->launchThread();
   return;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Shutdown and delete threads. This is called at dll finalization.

void proc_finalize()
{
   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Shutdown program threads for simulation mode and exit.

   // Shutdown and delete thread.
   BackEnd::gInterfaceThread->shutdownThread();
   delete BackEnd::gInterfaceThread;
   BackEnd::gInterfaceThread = 0;
}

